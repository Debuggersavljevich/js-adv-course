//Arrays - Sintaxis básica - Se cuenta desde 0, como todo INDEX
var listaDeAnimales = ["Tigre", "Gato", "Oso", "Leon"];

console.log(listaDeAnimales.shift()) // Shifteamos el primer valor
console.log(listaDeAnimales.pop()) // Sacamos el último valor
console.log(listaDeAnimales.push("Orangután")) //Agregamos un objeto adentro del array
console.log(listaDeAnimales.concat(["Tiburón", "Ballena"])); // Concatenamos otro array dentro del primero, pero para que se printee tenemos que asignarlo a una función! 






